     &                              4,3,1,2,                            &
     &                              4,3,2,1 /),(/ 4, 24/) ) )

!-----------------------------------------------------------------
!
      if (intorder .gt. maxintorder) then
!  fetch the Gauss-Legendre quadrature abscissae and weights
        call get1Dintegpoints(intorder+2, x1Du, w1Du, npkt1Du, errcode)
        call get1Dintegpoints(intorder, x1Dw, w1Dw, npkt1Dw, errcode)
        if (mod(intorder,2) .eq. 0) then
!  even case
          allocate(x1dv,source=x1Dw)
          allocate(w1dv,source=w1Dw)
          npkt1Dv=npkt1Dw
        else
!  odd case
          allocate(x1dv,source=x1Du)
          allocate(w1dv,source=w1Du)
          npkt1Dv=npkt1Du
        end if
        if (errcode .eq. 1001) then
!  there is no such rule available for 1D 
          deallocate(w1Du, x1Du, w1Dv, x1Dv, w1Dw, x1Dw)
          return
        end if
!  the resulting npkt in 3D from the tensor product operation is the square of 1D points
        npkt = npkt1Du*npkt1Dv*npkt1Dw
        allocate(weig(npkt), lambda(4, npkt), STAT=istat)
        if (istat .ne. 0) then
          errcode =1007
        end if
!  construct the 3D abscissae and weights with some coordinates transformations:
!  1D of (x) --> 3D "collapsed coordinates" of (u,v,w) --> 3D volume coordinates
        do i=1, npkt1Du
          u=x1Du(i)
          do j=1, npkt1Dv
            v=x1Dv(j)
            do k=1, npkt1Dw
              w=x1Dw(k)
              index = k + ((i-1)*npkt1Dv + (j-1)) *npkt1Dw
              weig(index)   = w1Du(i) * w1Dv(j) * w1Dw(k)               &
     &                        * 3._DP/32._DP * (1._DP-u)**2 * (1._DP-v)
              lambda(1,index) = (1._DP+u)/2._DP
              lambda(2,index) = (1._DP-u)/4._DP * (1._DP+v)
              lambda(3,index) = (1._DP-u)/8._DP * (1._DP-v) * (1._DP+w)
              lambda(4,index) = 1._DP - lambda(1,index)                 &
     &                                - lambda(2,index) - lambda(3,index)
            end do
          end do
        end do
        deallocate(w1Du, x1Du, w1Dv, x1Dv, w1Dw, x1Dw)
        return
      end if
!
      errcode = 0
      istart = begin(intorder)
      istop = istart + number(intorder) - 1
!
      npkt = sum(multi(istart:istop))
!
      allocate( weig( npkt ), lambda( 4, npkt ), STAT=istat)
      if (istat .ne. 0) then
        errcode =1007
      end if
!
      k = 0
      do i = istart, istop
        select case (multi(i))
        case (1)
          k=k+1
          lambda( 1:4, k ) = xsi( 1:4, i )
          weig(k)=weight(i)
        case (4)
          forall( j = 1:4 )
            lambda( 1:4, k+j ) = xsi( xi4(:,j), i )
            weig(k+j)=weight(i)
          end forall
          k = k + 4
        case (6)
          forall( j = 1:6 )
            lambda( 1:4, k+j ) = xsi( xi6(:,j), i )
            weig(k+j)=weight(i)
          end forall
          k = k + 6
        case (12)
          forall( j = 1:12 )
            lambda( 1:4, k+j ) = xsi( xi12(:,j), i )
            weig(k+j)=weight(i)
          end forall
          k = k + 12
        case (24)
          forall( j = 1:24 )
            lambda( 1:4, k+j ) = xsi( xi24(:,j), i )
            weig(k+j)=weight(i)
          end forall
          k = k + 24
        end select
      end do

      return
      end subroutine get3Dintegpoints
